

public class CD {
  public String mArtistName;
  public String mAlbum;
  public Integer mReleaseYear;
  public Integer mPrice;

  public CD(String name, String album, Integer releaseYear, Integer price){
    mArtistName = name;
    mAlbum = album;
    mReleaseYear = releaseYear;
    mPrice = price;
  }
}
